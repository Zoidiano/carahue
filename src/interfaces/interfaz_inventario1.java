    public static void CargarTablas(int numero,String dato)
    {
        switch(numero)
        {
            case 1: sql.CargarTablaproductos(1, "",0);
                break;
            case 2: sql.CargarTablaproductos(2, dato,0);
                break;
            case 3: sql.CargarTablaproductos(3, dato,0);
                break;
        }
    }

